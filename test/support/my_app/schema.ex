defmodule MyApp.Schema do
  use Ecto.Schema
  import Ecto.Changeset

  schema "test_table" do
    field :autogenerated, Once, autogenerate: true

    field :bigint_id, Once

    field :bin_id, Once, db_format: :raw
    field :string_id, Once, db_format: :encoded

    # mysql-only
    field :unsigned_id, Once, db_format: :unsigned, load_in_query: false
  end

  def changeset(struct_or_cs \\ %__MODULE__{}, params) do
    struct_or_cs |> cast(params, [:bin_id, :bigint_id, :string_id, :unsigned_id])
  end
end
